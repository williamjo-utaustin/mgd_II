import numpy as np
import matplotlib.pyplot as plt


data_esbgk_nd = np.genfromtxt("problem_g_esbgk_Dprof.dat", delimiter = ",")
data_rg_nd = np.genfromtxt("problem_a_Dprof.dat", delimiter = ",")
plt.rcParams['figure.figsize'] = (6, 6)

plt.plot(data_esbgk_nd[:,0], data_esbgk_nd[:,9], label = "ES-BGK-PM", linewidth = 2)
plt.plot(data_rg_nd[:,0], data_rg_nd[:,9], label = "BGK-PM", linewidth = 2)
plt.xlim(0,75)
plt.legend(fontsize = 14)
plt.grid()
plt.xlabel(r'$\hat{x}$', fontsize = 14)
plt.ylabel(r'$\hat{n}$', fontsize = 14)
plt.xticks(fontsize = 14)
plt.yticks(fontsize = 14)
plt.tight_layout()
plt.savefig("problem_g_n.png")
plt.show()

