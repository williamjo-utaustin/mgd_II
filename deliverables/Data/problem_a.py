import numpy as np
import matplotlib.pyplot as plt

data_u = np.genfromtxt("problem_a_Uprof.dat", delimiter = ",")
data_nd = np.genfromtxt("problem_a_Dprof.dat", delimiter = ",")
data_T = np.genfromtxt("problem_a_Tprof.dat", delimiter = ",")

