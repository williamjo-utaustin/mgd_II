plt.plot(data_u[:,0], data_u[:,7] , label = r'$\hat{t} = 30$', linewidth = 2)
plt.plot(data_u[:,0], data_u[:,9] , label = r'$\hat{t} = 40$', linewidth = 2)
plt.plot(data_u[:,0], data_u[:,11], label = r'$\hat{t} = 50$', linewidth = 2)
plt.xlim(0,75)
plt.grid()
plt.legend(fontsize = 14)
plt.xlabel(r'$\hat{x}$', fontsize = 14)
plt.ylabel(r'$\hat{u_{x}}$', fontsize = 14)
plt.xticks(fontsize = 14)
plt.yticks(fontsize = 14)
plt.tight_layout()
plt.savefig("problem_a_u.png")
plt.show()

